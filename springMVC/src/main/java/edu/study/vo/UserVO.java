package edu.study.vo;

public class UserVO {
	private int uno;
	private String uid;
	private String upw;
	private String uname;
	private String ugen;
	private String uretire;
	private String ujoindate;
	private String ucode;
	
	public int getUno() {
		return uno;
	}
	public void setUno(int uno) {
		this.uno = uno;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getUpw() {
		return upw;
	}
	public void setUpw(String upw) {
		this.upw = upw;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getUgen() {
		return ugen;
	}
	public void setUgen(String ugender) {
		this.ugen = ugender;
	}
	public String getUretire() {
		return uretire;
	}
	public void setUretire(String uretire) {
		this.uretire = uretire;
	}
	public String getUjoindate() {
		return ujoindate;
	}
	public void setUjoindate(String ujoindate) {
		this.ujoindate = ujoindate;
	}
	public String getUcode() {
		return ucode;
	}
	public void setUcode(String ucode) {
		this.ucode = ucode;
	}
}
